package org.dtristu.javaocr.documentsretriever.dto;

public class FileDTO {
    private String name;
    private String id;

    public FileDTO() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
}
